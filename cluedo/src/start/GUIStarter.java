package start;

import ui.SetupUI;
import ui.UIController;

/**
 * This class is the starter class for the GUI. Use this in combination with
 * the GUI controller.
 * 
 * @see ui.GUIController
 */
public class GUIStarter {

	/**
	 * Main runner method. Starts the graphical user interface. This includes
	 * displaying a window for choosing the player in the game and preparing
	 * the main window.
	 * 
	 * @param args no arguments expected
	 */
	public static void main(String[] args) {
		UIController.switchToGUI();
		new SetupUI();
	}
}